								<p style="font-size: 18px;">В начале 1938 года на смену "Иж-7" пришла новая модель "Иж-8". 300-кубовый двигатель получил петлевую продувку с двумя продувочными каналами, за счет чего мощность повысилась с 5,5 до 8 л.с. Совершеннее стала и система электрооборудования - вместо маховичного магнето машина стала оборудоваться генератором и аккумулятором.

Мотоцикл оснастили амперметром и отдельным переключателем света. На первых экземплярах они располагались в специальном щитке над рулем, затем амперметр переместился в корпус фары, а переключатель света на правую сторону рамы под бензобак. Маховичное магдино закрывалось специальным кожухом. Внешне мотоцикл очень легко отличить от предшественника по его выпускной системе: расширительная камера стала компактней, и выхлопная труба выходила из нее не вперед, а назад. Тормозные барабаны колес усилили накладными бандажами. Инструментальный ящик перенесли из-под седла на правую заднюю часть рамы мотоцикла; появился багажник. Дополнения утяжелили машину на 8 кг, но максимальная скорость с увеличением мощности возросла до 90 км/ч.

Также завод изменил конструкцию пробки горловины бензобака: она стала штампованной и запиралась поворотом на 180 градусов вместо резьбовой закрутки ранее.


Поставив ИЖ-8 в массовое производство, заводчане увеличили план на 1000 машин. Спрос населения на эту машину повлек очередную коррекцию плана: в 1939 году до 4300 мотоциклов. К началу того же года конструкторский отдел спроектировал новый двухтактный двигатель рабочим объемом 350 см. кубических и мощностью более 10 л. с. для нового мотоцикла ИЖ-9. Так завершилась долгая работа по совершенствованию рекомендованного Автодором Л-300.
