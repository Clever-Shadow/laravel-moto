								<p style="font-size: 18px;">А в 1940 году на Ижевском мотозаводе началось производство модели "Иж-9", с увеличенной до 9 л.с. мощностью при 4 тысяч оборотов в минуту. Оснащался одноцилиндровым двигателем, цилиндр имел 2 выпускных окна. Рабочий объём двигателя состовлял 300 кубических сантиметров. Мотоцикл имеет массу 147 килограмм. В иж 9 передние тормозные барабаны были улучшены, их увеличили в диаметре. Топливный бак мотоцикла имеет объём 14 литров. На ИЖ-9 ставили новую улучшенную переднюю вилку, демпфер и верхний мостик.  На ИЖ-9 изменили систему продувки цилиндра, направили выхлопные газы в два глушителя, улучшили охлаждение цилиндра и головки блока, в шатуне применили игольчатый подшипник, создали более прочную и надежную вилку переднего колеса. В итоге новая машина по основным характеристикам уверенно вышла на мировой уровень.

