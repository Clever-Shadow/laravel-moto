								<p style="font-size: 18px;">Мотоцик ИЖ-7 - серийный мотоцикл, изготовленный Ижевским мотозаводом - первым заводом изготовителем первых в России мотоциклов.       

К 1 мая 1933 года были собраны опытные экземпляры легких мотоциклов "Иж-7" по документации, полученной из Ленинграда. Фактически это были двойники модели "Л-300" (та самая, разработанная Можаровым копия "ДКВ-Люксус-300") - двухтактный одноцилиндровый двигатель 300 куб.см. с поперечной продувкой (охлаждение двигателя) мощностью 6,5 л.с., трехступенчатая коробка передач, привод на заднее колесо цепью, рама из штампованных элементов, собранная на болтах, параллелограммная передняя вилка, неподрессоренное заднее колесо.

Но "Иж-7" подвергся печально знакомой нам операции "снижения трудоемкости": исчезли багажник и передние щитки, плоские подножки были заменены на круглые, вращающуюся ручку газа заменила поворотная манетка, привод декомпрессора не дистанционный, а краником на головке цилиндра.

В конце концов, для серийного производства был избран именно "Иж-7". Правда, из 12 мотоциклов, изготовленных в 1933 году, почти все вернулись на завод - подвела система зажигания. Но вскоре "детские болезни" были преодолены, и в 1934 году завод выпустил уже 111 мотоциклов. Работами руководил выпускник Московского механического института Рогожин В.В., вскоре ставший главным конструктором и временно главным технологом ОМЗ

